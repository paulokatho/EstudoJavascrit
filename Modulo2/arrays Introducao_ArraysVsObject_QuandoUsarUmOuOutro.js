// Array vs Oject - Quando usar um e quando usar outro!

/*
	Array: Acesso os dados atraves de um índice numérico.
	Oject: Acesso através de uma string, exemplo: 'obj.nome'
*/

var foxArray = ['Fox', 'Prata', 'VW'];
	console.log('Modelo:', foxArray[0]); //acessa por indice numerico
	console.log('');


var foxObject = {modelo: 'Fox', cor: 'Prata', fabricante: 'VW'};
	console.log('Cor:', foxObject.cor);
	console.log('');

